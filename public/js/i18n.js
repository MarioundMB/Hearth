// ─── Hilfsfunktionen ─────────────────────────────────────────────────────────

let _lang = 'en';

/** Übersetzt einen Key, ersetzt {platzhalter} mit vars-Objekt */
function t(key, vars) {
  const dict = I18N[_lang] || I18N.de;
  let str = dict[key] ?? I18N.de[key] ?? key;
  if (vars) Object.keys(vars).forEach((k) => { str = str.replace('{' + k + '}', vars[k]); });
  return str;
}

/** Wendet die Sprache auf alle data-i18n-Elemente im DOM an */
function applyLang(lang) {
  _lang = (lang && I18N[lang]) ? lang : 'en';
  document.documentElement.lang = _lang;
  try { localStorage.setItem('hearth-lang', _lang); } catch (_) {}

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
}

/** Sofort aus localStorage anwenden (verhindert Flackern) */
(function () {
  try {
    const saved = localStorage.getItem('hearth-lang');
    if (saved && I18N[saved]) applyLang(saved);
  } catch (_) {}
})();
